public class TernaryOperator{
   public static void main(String[] args){
   
   boolean isCar = false;
   if (isCar) {
      System.out.println("isCar is not equal to true.");
      }
      
   isCar = true;
   boolean wasCar = isCar ? true : false;
   if (wasCar){
      System.out.println("wasCar is true");
      }
   }
}   
